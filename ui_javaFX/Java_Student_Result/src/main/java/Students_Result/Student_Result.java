    private void checkTabelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkTabelActionPerformed
         double[] R = new double[14];
       
       R[0] = Double.parseDouble(jTextField13.getText());
       R[1] = Double.parseDouble(jTextField8.getText());
       R[2] = Double.parseDouble(jTextField9.getText());
       R[3] = Double.parseDouble(jTextField10.getText());
       R[4] = Double.parseDouble(jTextField12.getText());
       R[5] = Double.parseDouble(jTextField14.getText());
       
       
       R[8] = (R[0] + R[1] + R[2] + R[3] + R[4] + R[5]) / 6;
       R[9] = R[0] + R[1] + R[2] + R[3] + R[4] + R[5];
       String Average = String.format("%.0f", R[8]);
        jTextField5.setText(Average);
        
        String TotalScore = String.format("%.0f", R[9]);
        jTextField4.setText(TotalScore);
    }//GEN-LAST:event_checkTabelActionPerformed

